function arrayFix(argArray){
	for(let n = 0; n < argArray.length; n++){

		let dateString = argArray[n][0];
		dateString = dateString.split(" ");
		let dateMonth = dateString[0];
		switch(dateMonth){
			case "January":
				dateMonth = "01";
				break;
			case "February":
				dateMonth = "02";
				break;
			case "March":
				dateMonth = "03";
				break;
			case "April":
				dateMonth = "04";
				break;
			case "May":
				dateMonth = "05";
				break;
			case "June":
				dateMonth = "06";
				break;
			case "July":
				dateMonth = "07";
				break;
			case "August":
				dateMonth = "08";
				break;
			case "September":
				dateMonth = "09";
				break;
			case "October":
				dateMonth = "10";
				break;
			case "November":
				dateMonth = "11";
				break;
			case "December":
				dateMonth = "12";
				break;
		}
		let dateDay = dateString[1];
		if(dateDay < 10){
			dateDay = "0"+dateDay
		}
		argArray[n][0] = "00"+dateString[3]+"-"+dateMonth+"-"+dateDay;

		let blackoutTime = argArray[n][1];
		let hour = blackoutTime*24;
		let minute = hour%1;
		hour = Math.floor(hour);
		minute = minute*60;
		let second = minute%1;
		minute = Math.floor(minute);
		second = Math.round(second*60);
		if(second >= 60){
			second -= 60;
			minute += 1;
		}
		if(minute >= 60){
			minute -= 60;
			hour += 1;
		}
		if(second < 10){
			second = "0"+second;
		}
		if(minute < 10){
			minute = "0"+minute;
		}
		let amOrPm = "AM";
		if(hour >= 11){
			amOrPm = "PM";
			hour = hour-12;
		}
		if(hour == 0){
			hour = 12;
		}
		if(hour < 10){
			hour = "0"+hour;
		}
		argArray[n][1] = hour+":"+minute+":"+second+" "+amOrPm;

		let blackoutTimeB = argArray[n][2];
		let hourB = blackoutTimeB*24;
		let minuteB = hourB%1;
		hourB = Math.floor(hourB);
		minuteB = minuteB*60;
		let secondB = minuteB%1;
		minuteB = Math.floor(minuteB);
		secondB = Math.round(secondB*60);
		if(secondB >= 60){
			secondB -= 60;
			minuteB += 1;
		}
		if(minuteB >= 60){
			minuteB -= 60;
			hourB += 1;
		}
		if(secondB < 10){
			secondB = "0"+secondB;
		}
		if(minuteB < 10){
			minuteB = "0"+minuteB;
		}
		if(hourB < 10){
			hourB = "0"+hourB;
		}
		argArray[n][2] = hourB+":"+minuteB+":"+secondB;
	}

	return argArray;
}

